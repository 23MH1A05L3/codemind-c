#include<stdio.h>
int main()
{
    int n,n1,n2,s1,s2,r1,r2,r;
    scanf("%d",&n);
    n1=n;
    while(n!=0)
    {
        r=n%10;
        r1=r1*10+r;
        n=n/10;
    }
    s1=n1*n1;
    s2=r1*r1;
    n2=s2;
    while(s2!=0)
    {
        r=s2%10;
        r2=r2*10+r;
        s2=s2/10;
    }
    if(s1==r2)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}